function [ centimeters ] = c2iConverter( inches )
%{
c2iConverter - Converts inches to centimeters
Authors:    Bram Hampton
Assignment: Your Own Program 3 - convert
Changed:    19 September 2017
Purpose:
  Takes the input (inches) and converts to centimeters by multiplying the
  input by 2.54.
%}

centimeters = inches * 2.54;

end

